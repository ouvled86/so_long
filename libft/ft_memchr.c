#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*b;
	size_t			i;

	i = 0;
	b = (unsigned char *)s;
	while (i < n)
	{
		if ((unsigned char)c == b[i])
			return (&b[i]);
		i++;
	}
	return (NULL);
}

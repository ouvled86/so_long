#include "../includes/libft.h"

int	ft_isdigit(int c)
{
	if (c >= '0' && c <= '9')
		return (1);
	return (0);
}

int	ft_isdigit_signed(int c)
{
	if (ft_isdigit(c) || c == '-' || c == '+')
		return (1);
	return (0);
}
